import React, { useEffect } from 'react';
import { Link, useParams } from 'react-router-dom';
import { Button, Row, Col } from 'reactstrap';
import { Translate } from 'react-jhipster';
import { FontAwesomeIcon } from '@fortawesome/react-fontawesome';

import { useAppDispatch, useAppSelector } from 'app/config/store';

import { getEntity } from './config-type.reducer';

export const ConfigTypeDetail = () => {
  const dispatch = useAppDispatch();

  const { id } = useParams<'id'>();

  useEffect(() => {
    dispatch(getEntity(id));
  }, []);

  const configTypeEntity = useAppSelector(state => state.configType.entity);
  return (
    <Row>
      <Col md="8">
        <h2 data-cy="configTypeDetailsHeading">
          <Translate contentKey="quanfigApp.configType.detail.title">ConfigType</Translate>
        </h2>
        <dl className="jh-entity-details">
          <dt>
            <span id="id">
              <Translate contentKey="global.field.id">ID</Translate>
            </span>
          </dt>
          <dd>{configTypeEntity.id}</dd>
          <dt>
            <span id="conType">
              <Translate contentKey="quanfigApp.configType.conType">Con Type</Translate>
            </span>
          </dt>
          <dd>{configTypeEntity.conType}</dd>
        </dl>
        <Button tag={Link} to="/config-type" replace color="info" data-cy="entityDetailsBackButton">
          <FontAwesomeIcon icon="arrow-left" />{' '}
          <span className="d-none d-md-inline">
            <Translate contentKey="entity.action.back">Back</Translate>
          </span>
        </Button>
        &nbsp;
        <Button tag={Link} to={`/config-type/${configTypeEntity.id}/edit`} replace color="primary">
          <FontAwesomeIcon icon="pencil-alt" />{' '}
          <span className="d-none d-md-inline">
            <Translate contentKey="entity.action.edit">Edit</Translate>
          </span>
        </Button>
      </Col>
    </Row>
  );
};

export default ConfigTypeDetail;
